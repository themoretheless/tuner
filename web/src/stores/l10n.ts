export function useL10n() {
  const t = (key: string) => {
    if (lang.value === 'en' && en[key]) return en[key]
    return ru[key] ?? key
  }
  const toggleLang = () => {
    lang.value = lang.value === 'ru' ? 'en' : 'ru'
    try { localStorage.setItem('lang', lang.value) } catch { /* ignore */ }
  }
  return { lang, t, toggleLang }
}
